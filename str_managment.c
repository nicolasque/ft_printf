#include "ft_printf.h"

size_t	ft_strlen(char *str)
{
	size_t	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

void	ft_putstr(char *s)
{
	if (!s)
		write(1, "(null)", 6);
	else
		write(1, s,ft_strlen(s));
}

